/**
 * 子菜单在 children.length >= 1 时展示
 *
 * hidden: true                   设置是否在菜单中展示
 * alwaysShow: true               一直显示根路由
 * redirect: noRedirect           当设置 noRedirect 的时候该路由在面包屑导航中不可被点击
 * name:'router-name'             必填
 * meta : {
    roles: ['admin','editor']    该路由的权限
    title: 'title'               路由在侧边栏和面包屑中展示的名字
    icon: 'svg-name'/'el-icon-x' 侧边栏图标
    breadcrumb: false            是否在面包屑中展示，默认展示
    activeMenu: '/example/list'  高亮相对应的侧边栏
  }
 */

